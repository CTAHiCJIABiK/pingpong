ракетка_2 = pygame.Rect(ширина - 50 - ширина_ракетки, высота // 2 - высота_ракетки // 2, ширина_ракетки, высота_ракетки)

# Мяч
мяч = pygame.Rect(ширина // 2 - радиус_мяча // 2, высота // 2 - радиус_мяча // 2, радиус_мяча, радиус_мяча)

